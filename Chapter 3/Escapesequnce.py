# Example usage
print("Hello, world!")  # Normal string
print("Hello,\nworld!")  # Newline
print("Hello,\tworld!")  # Tab
print("She said, \"Hello!\"")  # Double quotes within double quotes
print('He\'s a programmer.')  # Single quote within single quotes
print("C:\\path\\to\\file.txt")  # Literal backslashes



# Newline (\n): Represents a newline character. When included in a string, it creates a new line.
# Tab (\t): Represents a tab character. It adds horizontal spacing.
# Backslash (\\): Represents a literal backslash character.
# Single Quote (\'): Represents a single quote character within a string enclosed in single quotes.
# Double Quote (\"): Represents a double quote character within a string enclosed in double quotes.
# Carriage Return (\r): Represents a carriage return character (used in some systems).
# Backspace (\b): Represents a backspace character.
# Form Feed (\f): Represents a form feed character (used in some systems).
# Octal Value (\ooo): Represents a character using its octal value (e.g., \101 represents the letter ‘A’).
# Hex Value (\xhh): Represents a character using its hexadecimal value (e.g., \x41 also represents the letter ‘A’)


